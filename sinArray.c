void computeSinCos(int *s, int *c, int angle)
{
    switch(angle / 90)
    {
        case 0: 
            *s = sinArray[angle]; 
            *c = sinArray[90 - angle]; 
            break;
        case 1: 
            *s = sinArray[180 - angle]; 
            *c = -sinArray[angle - 90]; 
            break;
        case 2: 
            *s = -sinArray[angle - 180]; 
            *c = -sinArray[270 - angle]; 
            break;
        case 3: 
            *s = -sinArray[360 - angle]; 
            *c = sinArray[angle - 270]; 
            break;
        default: 
            break;
    }
}
